    def _ensure_semantic_index(self) -> None:
        if self._semantic_index is not None:
            return

        import chromadb
        self._semantic_index = chromadb.Client(
            ChromaSettings(anonymized_telemetry=False)
        )

        # try to reuse existing collection
        try:
            self._semantic_index.delete_collection("knowledge_hub")
        except Exception:
            pass

        collection = self._semantic_index.create_collection(
            name="knowledge_hub",
            metadata={"hnsw:space": "cosine"},
        )

        model = self._get_embedding_model()
        docs_to_index = []

        for kb_name, kb in self.kbs.items():
            for rel_path, text in kb.documents.items():
                doc_id = f"{kb_name}::{rel_path}"
                docs_to_index.append((doc_id, kb_name, rel_path, text))

