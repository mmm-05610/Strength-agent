class KnowledgeBase:
    def __init__(self, name: str, root_path: Path, metadata: dict):
        self.name = name
        self.description = metadata.get("description", "")
        self.tags = metadata.get("tags", [])
        self.version = metadata.get("version", "0.1.0")
        self.root_path = root_path
        self.documents: dict[str, str] = {}          # relative_path -> full text
        self._titles: dict[str, str] = {}            # relative_path -> extracted title
        self._token_index: dict[str, set[str]] = {}  # token -> set of paths

    # -- loading ----------------------------------------------------------

    def load(self) -> int:
        count = 0
        for root, _dirs, files in os.walk(self.root_path):
            for fname in files:
                if not fname.lower().endswith(".md"):
                    continue
                full_path = Path(root) / fname
                try:
                    text = full_path.read_text(encoding="utf-8")
                except (OSError, UnicodeDecodeError):
                    continue

                rel = str(full_path.relative_to(self.root_path)).replace("\\", "/")
                self.documents[rel] = text
                self._titles[rel] = self._extract_title(text)
                self._index_document(rel, text)
                count += 1
        return count

    def reload(self) -> int:
        self.documents.clear()
        self._titles.clear()
        self._token_index.clear()
        return self.load()

    # -- search -----------------------------------------------------------

    def search(self, query: str, top_k: int = 10) -> list[SearchResult]:
        query_tokens = self._tokenize(query)
        if not query_tokens:
            return []

        scored: list[tuple[float, str]] = []
        for path in self.documents:
            score = 0.0
            hit_tokens: set[str] = set()
            for qt in query_tokens:
                if qt in self._token_index and path in self._token_index[qt]:
                    score += 1.0
                    hit_tokens.add(qt)
            if score > 0:
                # boost exact phrase match
                if query.lower() in self.documents[path].lower():
                    score += 2.0
                # normalize by document length
                length = len(self.documents[path])
                if length > 0:
                    score = score * (min(len(query_tokens), len(hit_tokens)) / max(len(query_tokens), 1))
                scored.append((score, path))

        scored.sort(key=lambda x: x[0], reverse=True)
        results = []
        for score, path in scored[:top_k]:
            snippet = self._make_snippet(self.documents[path], query)
            results.append(SearchResult(
                kb_name=self.name,
                file_path=path,
                title=self._titles.get(path, path),
                snippet=snippet,
                score=round(score, 3),
            ))
        return results

