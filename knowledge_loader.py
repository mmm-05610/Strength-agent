    # -- internals --------------------------------------------------------

    @staticmethod
    def _tokenize(text: str) -> list[str]:
        return re.findall(r"\w{2,}", text.lower())

    @staticmethod
    def _extract_title(text: str) -> str:
        m = re.search(r"^#\s+(.+)$", text, re.MULTILINE)
        return m.group(1).strip() if m else "Untitled"

    def _index_document(self, path: str, text: str) -> None:
        for token in set(self._tokenize(text)):
            self._token_index.setdefault(token, set()).add(path)

    @staticmethod
    def _make_snippet(text: str, query: str, window: int = 200) -> str:
        idx = text.lower().find(query.lower())
        if idx == -1:
            # try first query word
            first_word = query.split()[0].lower()
            idx = text.lower().find(first_word)
        if idx == -1:
            return text[:window] + ("..." if len(text) > window else "")
        start = max(0, idx - window // 2)
        end = min(len(text), idx + len(query) + window // 2)
        snippet = text[start:end]
        if start > 0:
            snippet = "..." + snippet
        if end < len(text):
            snippet = snippet + "..."
        return snippet


