    def load_all(self) -> list[str]:
        loaded = []
        state = self._read_state()
        entries = state.get("entries", {})

        for kb_name in sorted(entries.keys()):
            kb_path = self.knowledge_dir / kb_name
            if not kb_path.is_dir():
                continue
            meta = self._read_kb_metadata(kb_path)
            kb = KnowledgeBase(kb_name, kb_path, meta)
            count = kb.load()
            self.kbs[kb_name] = kb
            loaded.append(kb_name)

        return loaded

    def load(self, name: str) -> KnowledgeBase | None:
        kb_path = self.knowledge_dir / name
        if not kb_path.is_dir():
            return None
        meta = self._read_kb_metadata(kb_path)
        kb = KnowledgeBase(name, kb_path, meta)
        kb.load()
        self.kbs[name] = kb
        return kb

    def reload(self) -> list[str]:
        self.kbs.clear()
        self._semantic_index = None
        self._semantic_paths.clear()
        self._embedding_model = None
        return self.load_all()

    # -- keyword search ---------------------------------------------------

    def search(self, query: str, kb_name: Optional[str] = None,
               top_k: int = 10) -> list[SearchResult]:
        if kb_name:
            kb = self.kbs.get(kb_name) or self.load(kb_name)
            if kb is None:
                return []
            return kb.search(query, top_k=top_k)

        all_results: list[SearchResult] = []
        for kb in self.kbs.values():
            all_results.extend(kb.search(query, top_k=top_k))
        all_results.sort(key=lambda r: r.score, reverse=True)
